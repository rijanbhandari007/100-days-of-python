
#! variables are user assigned name that store the value that can be later used in the program. in python we can assign a value to a variable by using the assignment operator (=). the variable name should be descriptive and should not start with a number or contain any special characters.

name = "Rijan Bhandari"
age = 20
print(name)
print(age)

#! here name and age are variables that store the value "Rijan Bhandari" and 20 respectively. we can use these variables later in the program to display the information or perform any operations on them.